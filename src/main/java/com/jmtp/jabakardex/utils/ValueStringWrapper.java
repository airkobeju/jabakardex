package com.jmtp.jabakardex.utils;

public class ValueStringWrapper {

    private String value;

    public ValueStringWrapper() {
        super();
    }

    public ValueStringWrapper(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
